namespace Versit.VCard
{
    /// <summary>
    /// Different types of email addresses.
    /// </summary>
    public enum EmailType
    {
        /// <summary>
        /// Indicates America On-Line
        /// </summary>
        AOL,

        /// <summary>
        /// Indicates AppleLink
        /// </summary>
        AppleLink,

        /// <summary>
        /// Indicates AT and T Mail
        /// </summary>
        ATTMail,

        /// <summary>
        /// Indicates CompuServe Information Service
        /// </summary>
        CIS,

        /// <summary>
        /// Indicates eWorld
        /// </summary>
        eWorld,

        /// <summary>
        /// Indicates Internet SMTP (default)
        /// </summary>
        INTERNET,

        /// <summary>
        /// Indicates IBM Mail
        /// </summary>
        IBMMail,

        /// <summary>
        /// Indicates MCI Mail
        /// </summary>
        MCIMail,

        /// <summary>
        /// Indicates PowerShare
        /// </summary>
        POWERSHARE,

        /// <summary>
        /// Indicates Prodigy information service
        /// </summary>
        PRODIGY,

        /// <summary>
        /// Indicates Telex number
        /// </summary>
        TLX,

        /// <summary>
        /// Indicates X.400 service
        /// </summary>
        X400
    }
}
